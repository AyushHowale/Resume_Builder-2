// Validation rules for social login
export const validateSocialLogin = [
  body('provider')
    .isIn(['google', 'github', 'linkedin'])
    .withMessage('Invalid social provider'),
  
  body('name')
    .trim()
    .isLength({ min: 2, max: 50 })
    .withMessage('Name must be between 2 and 50 characters'),
  
  body('email')
    .isEmail()
    .withMessage('Please provide a valid email address')
    .normalizeEmail(),
  
  body('providerId')
    .notEmpty()
    .withMessage('Provider ID is required')
    .isLength({ max: 100 })
    .withMessage('Provider ID cannot exceed 100 characters'),
  
  body('avatar')
    .optional()
    .isURL()
    .withMessage('Avatar must be a valid URL')
];

